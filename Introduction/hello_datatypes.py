# BOOLEANS

hw = "Hello World!"
print(hw.isalpha())  # alphabetic characters only
print(hw.islower())
print(hw.endswith('!'))
print(hw.startswith('H'))
print(type(True), type(False), "\n")

x = None
y = None
print(x == y)
print(x is y, "\n")

print(True + True)
print(True * True)
print(True + False)
print(True * False)
print(False + False)
print(False * False, "\n")

